    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        boolean checkAdd = service.insertSrv(getCategoryFromJFrame());
        if (checkAdd) {
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            lstC = service.getSrv();
            LoadTable();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
            clearForm();
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        boolean checkAdd = service.updateSrv(Integer.parseInt(lblId.getText()) ,getCategoryFromJFrame());
        if (checkAdd) {
            JOptionPane.showMessageDialog(this, "Sửa thành công");
            lstC = service.getSrv();
            LoadTable();
        } else {
            JOptionPane.showMessageDialog(this, "Sửa thất bại");
            clearForm();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void tblDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDMouseClicked
        // TODO add your handling code here:
        int row = this.tblD.getSelectedRow();
        if (row == -1) {
            return;
        }
        String maDM = this.tblD.getValueAt(row, 0).toString();
        String ten = this.tblD.getValueAt(row, 1).toString();
        String mota = this.tblD.getValueAt(row, 2).toString();
        String trangthai = this.tblD.getValueAt(row, 3).toString();

        this.lblId.setText(maDM);
        this.txtTen.setText(ten);
        this.txtMoTa.setText(mota);
        this.txttrangThai.setText(trangthai);
       

    }//GEN-LAST:event_tblDMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        boolean checkAdd = service.deleteSrv(Integer.parseInt(lblId.getText()));
        if (checkAdd) {
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            lstC = service.getSrv();
            LoadTable();
        } else {
            JOptionPane.showMessageDialog(this, "Xóa thất bại");
            clearForm();
        }
    }//GEN-LAST:event_btnXoaActionPerformed

