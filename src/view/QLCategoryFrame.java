    private void setColumName(){
        model.setColumnCount(0);
        model.addColumn("ID");
        model.addColumn("Tên");
        model.addColumn("Mô tả");
        model.addColumn("Trạng thái");
    }
    
    private void clearForm(){
        lblId.setText("");
        txtTen.setText("");
        txtMoTa.setText("");
        txttrangThai.setText("");
    }
    
    private void LoadTable(){
        model.setRowCount(0);
        for (Category x : lstC) {
            Object row[] = new Object[]{
                x.getId(),
                x.getTen(),
                x.getMoTa(),
                x.getTrangThai()
            };
            model.addRow(row);
        }
    }
    
    private Category getCategoryFromJFrame(){
        return new Category(0, txtTen.getText(), txtMoTa.getText(), Integer.parseInt(txttrangThai.getText()));
    }
    
    
